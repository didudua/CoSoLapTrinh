a = int(input('Tieu thu='))
if 1 <= a <= 100:
    print("Phai tra=",(a*550)*1.1)
elif 101 <= a <= 150:
    print("Phai tra=",(100*550)+((a-100)*750)+((100*550)+((a-100)*750))*0.1)
elif 151 <= a <= 200:
    print("Phai tra=", (100*550)+(50*750)+((a-150)*950) +
          ((100*550)+(50*750)+((a-150)*950))*0.1)
else:
    print("Phai tra=",(100*550)+(50*750)+(50*950)+((a-200)*1350) +
          (((100*550)+(50*750)+(50*950)+((a-200)*1350)))*0.1)
